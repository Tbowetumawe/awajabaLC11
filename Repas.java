class Repas
{
	int numero ;
	LocalDate date; 
	
	public Repas(numero int, date LocalDate){
		this.numero=numero;
		this.date=date;
	}
	
	public int getNumero(){
		return numero;
		
	}
	
	public int SetNumero(int numero){
		this.numero;
	}
	
	
	public LocalDate getDate(){
		return date;
	}
	
	public LocalDate setDate(date LocalDate){
		this.date;
	}
};
